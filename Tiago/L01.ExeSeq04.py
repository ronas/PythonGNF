# Calculando a media entre 4 numeros.
print("Digite as notas bimestrais para calcular sua media")

bim1=input ("Digite a nota do 1 bimestre: ")
bim2=input ("Digite a nota do 2 bimestre: ")
bim3=input ("Digite a nota do 3 bimestre: ")
bim4=input ("Digite a nota do 4 bimestre: ")

