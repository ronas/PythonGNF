#Estruturas de decisão

salario1 = input('Salario 1: ') 
salario2 = input('Salario 2: ')

if salario1 > salario2:
    print ('Salario1 é maior que Salario2')

if salario1 < salario2:
    print ('Salario2 é maior que Salario1')

if salario1 == salario2:
    print ('Salario1 é igual ao Salario2 ')